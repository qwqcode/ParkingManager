package com.qwqcode.parkingmanager.entity;

import java.math.BigDecimal;
import java.util.Date;

public class User {
    public static final int ROLE_NORMAL = 10; // 普通用户
    public static final int ROLE_ADMIN = 20;  // 管理员

    public static final int STATUS_NORMAL = 1;  // 正常状态
    public static final int STATUS_DELETED = 0; // 已禁用状态

    private int id;
    private String phone;

    private String password;
    private String openid;
    private Date birthday;
    private String gender;
    private int credit = 0;
    private BigDecimal amount = new BigDecimal(0.00);
    private int role = ROLE_NORMAL;
    private int status = STATUS_NORMAL;
    private Date created_at;
    private Date updated_at;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public Date getBirthday() {
        return birthday;
    }

    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getCredit() {
        return credit;
    }

    public void setCredit(int credit) {
        this.credit = credit;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public Date getCreated_at() {
        return created_at;
    }

    public void setCreated_at(Date created_at) {
        this.created_at = created_at;
    }

    public Date getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(Date updated_at) {
        this.updated_at = updated_at;
    }
}
